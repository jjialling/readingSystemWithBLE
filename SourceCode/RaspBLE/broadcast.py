class MessageOneChrc(Characteristic):
    MS_UUID = '4116f8d2-9f66-4f58-a35d-fc7440e7c14e'

    def __init__(self, bus, index, service):
        Characteristic.__init__(
                self, bus, index,
                self.MS_UUID,
                ['notify','write','read'],
                service)
        self.notifying = False
        self.value = [0x01]

    def Send_value(self):
        if not self.notifying:
            return

        self.PropertiesChanged(GATT_CHRC_IFACE, { 'Value': self.value }, [])

        return self.notifying
    

    def StartNotify(self):
        if self.notifying:
            return
        self.notifying = True

    def StopNotify(self):
        if not self.notifying:
            return
        self.notifying = False
    
    def WriteValue(self, value, options):
        print('Characteristic Write: ' + repr(value))
        self.value = value
      
    def ReadValue(self, options):
        # Return 'Chest' as the sensor location.
        print('Characteristic Read: ' + repr(self.value))
        return self.value


class MessageTwoChrc(Characteristic):
    BODY_UUID = '322e774f-c903-49c4-bd7b-48a4003a967f'
    def __init__(self, bus, index, service):
        Characteristic.__init__(self, bus, index, self.BODY_UUID,
                                ['notify'], service)
        self.notifying = False
        GLib.io_add_watch(sys.stdin, GLib.IO_IN, self.on_console_input)

    def on_console_input(self, fd, condition):
        s = fd.readline()
       
        if s.isspace():
            pass
        else:
            self.send_tx(s)
        return True

    def send_tx(self, s):
        if not self.notifying:
            return
        value = []
        for c in s:
            value.append(dbus.Byte(c.encode()))
        self.PropertiesChanged(GATT_CHRC_IFACE, {'Value': value}, [])

    def StartNotify(self):
        if self.notifying:
            return
        self.notifying = True

    def StopNotify(self):
        if not self.notifying:
            return
        self.notifying = False
    
                

